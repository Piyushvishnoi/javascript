function trueorfalse(isThatTrue) {
  if (isThatTrue) return "Yes that is true";
  else return "Yes that is false";
}
console.log(trueorfalse(true));
function mytrueorfalse(wasThatFalse) {
  if (wasThatFalse) return "Yes that was false";
  else return "Yes that was true";
}
console.log(mytrueorfalse(false));
